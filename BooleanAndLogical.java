
public class BooleanAndLogical {

	public static void main(String[] args) {

		// checking boolean
		
		System.out.println(6>9);
		System.out.println(12<11);
		System.out.println(14>=3);
		System.out.println(15<=90);
		System.out.println(90==90);
		System.out.println(30!=20);
		
		
		//checking logical operator for AND operator
		System.out.println(7!=9 && 8==8);
		System.out.println(12>0 && 7==0);
		System.out.println(12!=12 && 12>=6);
		System.out.println(12>90 && 10==5);
		
		//checking logical operator for OR operator
		System.out.println(7!=9 || 8==8);
		System.out.println(12>0 || 7==0);
		System.out.println(12!=12 || 12>=6);
		System.out.println(12>90 || 10==5);
		
		//checking logical operator for not operator
		System.out.println(!(12>105));
		System.out.println(!(9<29));
		
		

	}

}
